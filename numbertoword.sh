#!/bin/bash -x

echo "enter a number : "
read number

if [ $number -eq 0 ]
 then
    echo  $number : "zero" 

elif [  $number -eq 1 ]
   then
    echo  $number : "one"

elif [  $number -eq 2 ]
   then
    echo  $number : "three"

elif [  $number -eq 3 ]
   then
    echo  $number : "three"

elif [  $number -eq 4 ]
   then
    echo  $number : "four"

elif [  $number -eq 5 ]
   then
    echo  $number : "five"

elif [  $number -eq 6 ]
   then
    echo  $number : "six"

elif [  $number -eq 7 ]
   then
    echo  $number : "seven"

elif [  $number -eq 8 ]
   then
    echo  $number : "eight"

elif [  $number -eq 9 ]
   then
    echo  $number : "nine"

else 
   echo "invalid number"

fi








